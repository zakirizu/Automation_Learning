package oops_Concepts;

abstract class Alpha_Abstarction extends Beta_Abstraction {

	public static void main(String[] args) {
		
	}
	
	
	
	//50% of Abstarction achievend here
			
	//Implemented Method
	public void addition() {
		int sum = 10+20;
		System.out.println("Addition "+sum);
	}
	
	//UnImplemented Method
	public abstract void modulus();
	
	
	//Implemented Method
	public void multiplicaiton() {	
		int multi = 9*6;
		System.out.println("Multiplication is "+multi);
	}
	
	//UnImplemented Method
	public abstract  void subraction();
	

	

	

}
